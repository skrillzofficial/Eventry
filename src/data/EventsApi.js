// API functions
export const eventsApi = {
  // Get all events
  getAllEvents: () => {
    return new Promise((resolve) => {
      setTimeout(() => {
        resolve(eventsData.events);
      }, 500);
    });
  },

  // Get event by ID
  getEventById: (id) => {
    return new Promise((resolve, reject) => {
      setTimeout(() => {
        const event = eventsData.events.find(
          (event) => event.id === parseInt(id)
        );
        if (event) {
          resolve(event);
        } else {
          reject(new Error("Event not found"));
        }
      }, 300);
    });
  },

  // Get featured events
  getFeaturedEvents: () => {
    return new Promise((resolve) => {
      setTimeout(() => {
        const featured = eventsData.events.filter((event) => event.featured);
        resolve(featured);
      }, 400);
    });
  },

  // Get events by category
  getEventsByCategory: (category) => {
    return new Promise((resolve) => {
      setTimeout(() => {
        const filtered = eventsData.events.filter(
          (event) => event.category.toLowerCase() === category.toLowerCase()
        );
        resolve(filtered);
      }, 400);
    });
  },

  // Get events by city
  getEventsByCity: (city) => {
    return new Promise((resolve) => {
      setTimeout(() => {
        const filtered = eventsData.events.filter(
          (event) => event.city.toLowerCase() === city.toLowerCase()
        );
        resolve(filtered);
      }, 400);
    });
  },

  // Search events
  searchEvents: (query) => {
    return new Promise((resolve) => {
      setTimeout(() => {
        const results = eventsData.events.filter(
          (event) =>
            event.title.toLowerCase().includes(query.toLowerCase()) ||
            event.description.toLowerCase().includes(query.toLowerCase()) ||
            event.tags.some((tag) =>
              tag.toLowerCase().includes(query.toLowerCase())
            )
        );
        resolve(results);
      }, 500);
    });
  },

  // Get related events (same category, excluding current event)
  getRelatedEvents: (currentEventId, limit = 3) => {
    return new Promise((resolve) => {
      setTimeout(() => {
        const currentEvent = eventsData.events.find(
          (event) => event.id === parseInt(currentEventId)
        );
        if (!currentEvent) {
          resolve([]);
          return;
        }

        const related = eventsData.events
          .filter(
            (event) =>
              event.id !== parseInt(currentEventId) &&
              event.category === currentEvent.category
          )
          .slice(0, limit);

        resolve(related);
      }, 400);
    });
  },

  // Get all categories
  getCategories: () => {
    return new Promise((resolve) => {
      setTimeout(() => {
        resolve(eventsData.categories);
      }, 200);
    });
  },

  // Get all cities
  getCities: () => {
    return new Promise((resolve) => {
      setTimeout(() => {
        resolve(eventsData.cities);
      }, 200);
    });
  },
};

export default eventsApi;
